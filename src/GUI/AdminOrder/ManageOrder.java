    private void dltOrderbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dltOrderbtnActionPerformed
        try {
            int choice = JOptionPane.showConfirmDialog(null, "Are you sure to delete this order?", "Delete", JOptionPane.YES_NO_OPTION);
            if (choice == 0) {
                OrderManager oManager = new OrderManager();
                orderList = oManager.read();
                int row = orderTbl.getSelectedRow();
                int selectedRow = orderTbl.convertRowIndexToModel(row); //get the right index after filtering the table
                orderList.remove(selectedRow);
                //print to file
                oManager.printOrder2File(orderList);
                addOrder2JTable();

                //display msg
                JOptionPane.showMessageDialog(this, "Delete Successfully");

            }

        } catch (IOException e) {
        }

    }//GEN-LAST:event_dltOrderbtnActionPerformed

    private void editOrderbtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editOrderbtn1ActionPerformed

        int row = orderTbl.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select row to update");
        } else {
            int selectedRow = orderTbl.convertRowIndexToModel(row);
            setVisible(false);
            new AddOrder(selectedRow).setVisible(true);
        }

    }//GEN-LAST:event_editOrderbtn1ActionPerformed

