    public void addOrder2JTable() {
        DefaultTableModel model = (DefaultTableModel) orderTbl.getModel();
        try {
            OrderManager oManager = new OrderManager();
            orderList = oManager.read();
            Object rowData[] = new Object[7];
            model.setRowCount(0);
            for (int i = 0; i < orderList.size(); i++) {
                rowData[0] = orderList.get(i).getOrderID();
                rowData[1] = orderList.get(i).getCustomer().getCusID();
                rowData[2] = orderList.get(i).getCustomer().name;
                rowData[3] = orderList.get(i).getDate();
                rowData[4] = orderList.get(i).getTime();
                ArrayList<String> tempArray = new ArrayList<>();
                for (int items = 0; items < orderList.get(i).orderItems.size(); items++) {
                    tempArray.add(orderList.get(i).orderItems.get(items).getProduct().getProductID());
                    tempArray.add(String.valueOf(orderList.get(i).orderItems.get(items).getQuantity()));
                }
                rowData[5] = tempArray;
                rowData[6] = orderList.get(i).getTotalPrice();
                model.addRow(rowData);
            }

        } catch (IOException e) {
            System.out.println("Error");
        }

    }

    public TableRowSorter<DefaultTableModel> sortOrderTable() {

        DefaultTableModel table = (DefaultTableModel) orderTbl.getModel();

        TableRowSorter<DefaultTableModel> sortOrderTable = new TableRowSorter<>(table);
        orderTbl.setRowSorter(sortOrderTable);
        return sortOrderTable;
    }

