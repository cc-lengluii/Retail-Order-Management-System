    private void updateProbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateProbtnActionPerformed
        if (txtProName.getText().equals("") || txtPrice.getText().equals("") || txtQuantity.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please fill in the blank");
            return;
        }
        if (new CustomerManager().isStringNumeric(txtPrice.getText()) || new CustomerManager().isStringNumeric(txtQuantity.getText())) {
            JOptionPane.showMessageDialog(null, "Please insert integer");
            return;
        }
        int row = productTbl.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select row to update");
        } else {
            ProductManager manager = new ProductManager();
            try {
                productList = manager.read();

                int selectedRow = productTbl.convertRowIndexToModel(row);

                productList.get(selectedRow).setProductName(txtProName.getText());
                productList.get(selectedRow).setCategory(jcbCategory.getSelectedItem().toString());
                productList.get(selectedRow).setType(jcbType.getSelectedItem().toString().charAt(0));
                String type = jcbType.getSelectedItem().toString();
                double price = Double.parseDouble(txtPrice.getText());
                if (type.equals("Fragile")) {
                    String fPrice = String.valueOf(df.format(price * 1.1));
                    productList.get(selectedRow).setPrice(Double.parseDouble(fPrice));
                } else {
                    productList.get(selectedRow).setPrice(price);
                }

                productList.get(selectedRow).setStock(Integer.parseInt(txtQuantity.getText()));

                //print to file
                manager.printProList2File(productList);

                JOptionPane.showMessageDialog(this, "Update Successfully");
                addRow2JTable();
                txtProName.setText("");
                jcbCategory.setSelectedIndex(0);
                jcbType.setSelectedIndex(0);
                txtPrice.setText("");
                txtQuantity.setText("");

            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "Error");
            }
        }

    }//GEN-LAST:event_updateProbtnActionPerformed

    private void deleteProbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteProbtnActionPerformed

        ProductManager manager = new ProductManager();
        try {
            int choice = JOptionPane.showConfirmDialog(null, "Are you sure to delete this product?", "Delete", JOptionPane.YES_NO_OPTION);

            if (choice == 0) {
                productList = manager.read();
                int row = productTbl.getSelectedRow();
                int selectedRow = productTbl.convertRowIndexToModel(row);
                ArrayList<Order> orderList = new OrderManager().read();
                for (int i = 0; i < orderList.size(); i++) {
                    for (int j = 0; j < orderList.get(i).orderItems.size(); j++) {
                        if (orderList.get(i).orderItems.get(j).getProduct().getProductID().equals(productList.get(selectedRow).getProductID())) {
                            JOptionPane.showMessageDialog(this, "Products that have been placed by the customer cannot be deleted");
                            return;
                        }

                    }
                }
                ArrayList<Cart> cartList = new CartManager().readcart();
                for (int i = 0; i < cartList.size(); i++) {
                    for (int j = 0; j < cartList.get(i).orderItems.size(); j++) {
                        if (cartList.get(i).orderItems.get(j).getProduct().getProductID().equals(productList.get(selectedRow).getProductID())) {
                            JOptionPane.showMessageDialog(this, "Products that have been placed by the customer cannot be deleted");
                            return;
                        }

                    }
                }
                productList.remove(selectedRow);
                //print to file 
                manager.printProList2File(productList);
                JOptionPane.showMessageDialog(this, "Delete Successfully");
            }
            addRow2JTable();
            txtProName.setText("");
            jcbCategory.setSelectedIndex(0);
            jcbType.setSelectedIndex(0);
            txtPrice.setText("");
            txtQuantity.setText("");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error");
        }

    }//GEN-LAST:event_deleteProbtnActionPerformed

