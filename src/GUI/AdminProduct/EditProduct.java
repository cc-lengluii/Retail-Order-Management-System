    public void addRow2JTable() {
        DefaultTableModel model = (DefaultTableModel) productTbl.getModel();
        ProductManager pManager = new ProductManager();
        try {
            productList = pManager.read();
            Object rowData[] = new Object[6];
            model.setRowCount(0);
            for (int i = 0; i < productList.size(); i++) {
                rowData[0] = productList.get(i).getProductID();
                rowData[1] = productList.get(i).getProductName();
                rowData[2] = productList.get(i).getCategory();
                rowData[3] = productList.get(i).getType();
                rowData[4] = productList.get(i).getPrice();
                rowData[5] = productList.get(i).getStock();
                model.addRow(rowData);

            }

        } catch (IOException e) {
            System.out.println("Error");
        }

    }

    public void displayProDetails() {

        DefaultTableModel model = (DefaultTableModel) productTbl.getModel();

        int selectedRow = productTbl.convertRowIndexToModel(productTbl.getSelectedRow());

        String proName = model.getValueAt(selectedRow, 1).toString();
        txtProName.setText(proName);

        String category = model.getValueAt(selectedRow, 2).toString();
        jcbCategory.setSelectedItem(category);

        String type = model.getValueAt(selectedRow, 3).toString();
        if (type.equals("F")) {
            jcbType.setSelectedItem("Fragile");
        } else {
            jcbType.setSelectedItem("Non-Fragile");
        }

        String price = model.getValueAt(selectedRow, 4).toString();
        if (model.getValueAt(selectedRow, 3).equals('F')) {
            double fPrice = Double.parseDouble(price) / 1.1;
            txtPrice.setText(String.valueOf(df.format(fPrice)));
        } else {
            txtPrice.setText(price);
        }

        String stock = model.getValueAt(selectedRow, 5).toString();
        txtQuantity.setText(stock);

    }

    public TableRowSorter<DefaultTableModel> sortTable() {

        DefaultTableModel model = (DefaultTableModel) productTbl.getModel();

        TableRowSorter<DefaultTableModel> sortTable = new TableRowSorter<>(model);
        productTbl.setRowSorter(sortTable);
        return sortTable;
    }

