    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        String searchCus = txtSearch.getText().trim();
        TableRowSorter<DefaultTableModel> search = sortTable();
        customerTbl.setRowSorter(search);

        search.setRowFilter(RowFilter.regexFilter("(?i)" + searchCus)); //ignore case sentitive       
    }//GEN-LAST:event_txtSearchKeyReleased

