    //print all customer object from arraylist to jtable
    public void addRow2JTable() {
        DefaultTableModel model = (DefaultTableModel) customerTbl.getModel();
        CustomerManager manager = new CustomerManager();
        try {
            ArrayList<Customer> customerList = manager.read();
            Object rowData[] = new Object[6];
            model.setRowCount(0);
            for (int i = 0; i < customerList.size(); i++) {
                rowData[0] = customerList.get(i).getCusID();
                rowData[1] = customerList.get(i).name;
                rowData[2] = customerList.get(i).getGender();
                rowData[3] = customerList.get(i).getPhoneNo();
                rowData[4] = customerList.get(i).getEmail();
                rowData[5] = customerList.get(i).address;
                model.addRow(rowData);

            }

        } catch (IOException e) {
            System.out.println("Error");
        }

    }

    //sort table by columns
    public TableRowSorter<DefaultTableModel> sortTable() {

        DefaultTableModel table = (DefaultTableModel) customerTbl.getModel();

        TableRowSorter<DefaultTableModel> sortTable = new TableRowSorter<DefaultTableModel>(table);
        customerTbl.setRowSorter(sortTable);
        return sortTable;
    }

    // display a particular customer details 
    public void showPosDetails(int index) {
        try {
            ArrayList<Customer> customerList = new CustomerManager().read();
            lblShowName.setText(customerList.get(index).name.getName());

            char gender = customerList.get(index).getGender();
            if (gender == 'F') {
                lblShowGender.setText("Female");

            } else {
                lblShowGender.setText("Male");
            }
            lblShowContact.setText(customerList.get(index).getPhoneNo());
            lblShowEmail.setText(customerList.get(index).getEmail());
            lblShowAddress.setText(customerList.get(index).address.getFullAddress());
        } catch (IOException e) {
        }

    }

