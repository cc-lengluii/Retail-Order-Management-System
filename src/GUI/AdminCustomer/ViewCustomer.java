    private void btnPreviosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPreviosActionPerformed

        RecNo--;
        if (RecNo > 0) {

            showPosDetails(RecNo);

        } else {

            RecNo = 0;
            showPosDetails(RecNo);
        }
    }//GEN-LAST:event_btnPreviosActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        try {
            ArrayList<Customer> customerList = new CustomerManager().read();
            RecNo++;
            if (RecNo < customerList.size()) {

                showPosDetails(RecNo);
            } else {

                RecNo = customerList.size() - 1;
                showPosDetails(RecNo);
                JOptionPane.showMessageDialog(null, "End");

            }
        } catch (IOException e) {
        }
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnLastActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLastActionPerformed

        try {
            ArrayList<Customer> customerList = new CustomerManager().read();
            RecNo = customerList.size() - 1; //Record Number
            showPosDetails(RecNo);
        } catch (IOException e) {
        }
    }//GEN-LAST:event_btnLastActionPerformed

